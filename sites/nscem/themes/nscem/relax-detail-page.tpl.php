                  <h5>Don’t wait for Turkey to start preparing</h5>

                  <p>The countdown is on: Thanksgiving is just around the corner. While your mouth may water when you envision the meal, your mind may race at the thought of all of the work involved in pulling off this holiday feast. Instead of worrying, start planning—now. The steps you take in the days leading up to Thanksgiving will make a difference in your stress level on Turkey Day.</p>

                  <h5>Map Out Your Menu </h5>

                  <p>While you may think you know what’s cooking on Thanksgiving, it pays to spell out what your feast will feature ahead of time. This will ensure no last-minute surprises and allow you adequate time to procure and prepare each item. There’s more to the meal than the turkey. Think through the entire dinner—including appetizers, side dishes, desserts, and drinks. Select the recipe you’ll be using for each dish and determine the correct amount to make for your guest count.</p>

                  <h5>Get Your List On </h5>

                  <p>An accurate shopping list is key to a successful Thanksgiving. Read over each recipe in your menu plan and create a shopping list a few days to a week in advance. If your shopping trip requires visits to multiple stores, create a separate list for each venue to help organize your efforts. Include any specific kitchen tools and gadgets—such as a turkey baster, meat thermometer, and roasting pan—and don’t forget to stock up on spices!</p>
